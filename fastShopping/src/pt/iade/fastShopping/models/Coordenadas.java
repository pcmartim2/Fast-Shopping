package pt.iade.fastShopping.models;

public class Coordenadas {

	private double coordX;
	private double coordY;

	public Coordenadas(double coordX, double coordY) {
		this.coordX = coordX;
		this.coordY = coordY;
	}

	public double getCoordX() {
		return coordX;
	}

	public double getCoordY() {
		return coordY;
	}

}
